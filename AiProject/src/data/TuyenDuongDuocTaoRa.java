package data;

public class TuyenDuongDuocTaoRa {
	private Xe xe;
	private TuyenDuong tuyenDuong;
	public TuyenDuongDuocTaoRa(Xe xe, TuyenDuong tuyenDuong) {
		this.xe = xe;
		this.tuyenDuong = tuyenDuong;
	}
	public Xe getXe() {
		return xe;
	}
	public void setXe(Xe xe) {
		this.xe = xe;
	}
	public TuyenDuong getTuyenDuong() {
		return tuyenDuong;
	}
	public void setTuyenDuong(TuyenDuong tuyenDuong) {
		this.tuyenDuong = tuyenDuong;
	}

}
